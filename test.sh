#!/bin/bash

# Встановлюємо інтервал у секундах
INTERVAL=60

# Нескінченний цикл для виведення повідомлення кожні INTERVAL секунд
while true; do
    # Виводимо повідомлення у системний журнал за допомогою команди logger
    logger "Це приклад повідомлення у системний журнал"

    # Засинаємо на INTERVAL секунд перед виведенням наступного повідомлення
    sleep $INTERVAL
done

